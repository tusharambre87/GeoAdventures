export type Difficulty = "EASY" | "MEDIUM" | "HARD";

export interface LetterRevealConfig {
  minRevealed: number;
  maxRevealed: number;
  percentage: number;
}

export const DIFFICULTY_CONFIG: Record<Difficulty, LetterRevealConfig> = {
  EASY: { minRevealed: 3, maxRevealed: 5, percentage: 0.5 },
  MEDIUM: { minRevealed: 2, maxRevealed: 3, percentage: 0.3 },
  HARD: { minRevealed: 1, maxRevealed: 2, percentage: 0.1 }
};

export function getRevealedLetters(word: string, difficulty: Difficulty): boolean[] {
  const config = DIFFICULTY_CONFIG[difficulty];
  const letters = word.toUpperCase().split('');
  const revealed: boolean[] = new Array(letters.length).fill(false);
  
  letters.forEach((letter, i) => {
    if (letter === ' ') {
      revealed[i] = true;
    }
  });
  
  const letterIndices = letters
    .map((letter, i) => ({ letter, index: i }))
    .filter(({ letter }) => letter !== ' ')
    .map(({ index }) => index);
  
  const actualLetterCount = letterIndices.length;
  const targetRevealed = Math.max(
    config.minRevealed,
    Math.min(config.maxRevealed, Math.ceil(actualLetterCount * config.percentage))
  );
  
  if (letterIndices.length > 0) {
    revealed[letterIndices[0]] = true;
  }
  
  let revealedCount = 1;
  
  if (revealedCount < targetRevealed && letterIndices.length > 2) {
    const middlePos = Math.floor(letterIndices.length / 2);
    revealed[letterIndices[middlePos]] = true;
    revealedCount++;
  }
  
  const availableIndices = letterIndices
    .filter(i => !revealed[i] && i !== letterIndices[letterIndices.length - 1]);
  
  while (revealedCount < targetRevealed && availableIndices.length > 0) {
    const randomIndex = Math.floor(Math.random() * availableIndices.length);
    const letterIndex = availableIndices.splice(randomIndex, 1)[0];
    revealed[letterIndex] = true;
    revealedCount++;
  }
  
  return revealed;
}

export function generateLetterChoices(correctLetter: string, count: number = 4): string[] {
  const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
  const choices = new Set<string>([correctLetter.toUpperCase()]);
  
  while (choices.size < count) {
    const randomLetter = alphabet[Math.floor(Math.random() * alphabet.length)];
    choices.add(randomLetter);
  }
  
  return Array.from(choices).sort(() => Math.random() - 0.5);
}

export function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

